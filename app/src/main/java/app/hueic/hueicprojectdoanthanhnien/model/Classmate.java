public class Classmate {

    private int IDClass;
    private String nameClass;
    private String descriptionClass;
    private int facultyIdClass;

    public int getIDClass() {
        return IDClass;
    }

    public void setIDClass(int IDClass) {
        this.IDClass = IDClass;
    }

    public String getNameClass() {
        return nameClass;
    }

    public void setNameClass(String nameClass) {
        this.nameClass = nameClass;
    }

    public String getDescriptionClass() {
        return descriptionClass;
    }

    public void setDescriptionClass(String descriptionClass) {
        this.descriptionClass = descriptionClass;
    }

    public int getFacultyIdClass() {
        return facultyIdClass;
    }

    public void setFacultyIdClass(int facultyIdClass) {
        this.facultyIdClass = facultyIdClass;
    }
}
